namespace SampleApp.ViewModels;

/// <summary>
/// Provides a enumeration for identifying the components of a <see cref="Color"/>.
/// </summary>
public enum ColorPart
{
    /// <summary>
    /// Identifies the <see cref="Color.Red"/> value.
    /// </summary>
    Red,
    /// <summary>
    /// Identifies the <see cref="Color.Green"/> value.
    /// </summary>
    Green,
    /// <summary>
    /// Identifies the <see cref="Color.Blue"/> value.
    /// </summary>
    Blue,
    /// <summary>
    /// Identifies the <see cref="Color.Alpha"/> value.
    /// </summary>
    Alpha
}

